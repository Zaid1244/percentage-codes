#include <iostream>
using namespace std;

int main() {
    int num1, num2, sum;

    // Input two numbers
    cout << "Enter first number: ";
    cin >> num1;

    cout << "Enter second number: ";
    cin >> num2;

    // Perform addition
    sum = num1 + num2;

    // Output result
    cout << "Sum = " << sum << endl;

    return 0;
}